		<h1>Stages</h1> 
 
		<p>AVID Elementary (AE) spans K-8 in three stages: Beginnings, Foundations, and Bridges. Each stage is designed to meet the needs of students within specific ranges of development.</p>

		<p><strong>AVID Elementary Beginnings</strong> addresses the early years of education, when students are emerging as learners: learning to read, learning to write, and learning to learn. AE Beginnings classrooms are designed to promote L-WICOR (Writing, Inquiry, Collaboration, Organization, and Reading) throughout the academic day.</p>

		<p><strong>AVID Elementary Foundations</strong> addresses the years of education, when students are becoming independent learners: reading to learn, writing to learn, and learning to reflect on their own learning. AE Foundations classrooms are designed to promote WICOR throughout the academic day.</p>

		<p><strong>AVID Elementary Bridges</strong> addresses the transitional years of education, when students are becoming independent thinkers: thinking about thinking and learning about their own learning. AE Bridges classrooms are designed to promote WICOR throughout the academic day.</p>


		
		<div class="divider-cont-bottom">
			<h3>Components </h3>
			<p>During each stage, AVID Elementary incorporates:</p>
		
			
			
			<p><strong>Student Success Skills</strong> – encompassing communication skills (e.g. listening, speaking, writing), self-advocacy skills, and study skills.</p>

			<p><strong>Organizational Skills</strong> – both mental and physical. Students are given organizational tools, time management, goal setting and note-taking strategies.</p>

			<p><strong>WICOR Lessons and L‐WICOR Lessons</strong> – emphasize instruction on writing to learn, inquiry, collaboration, organization and reading to learn in all content areas.</p>

			<p><strong>Partnerships</strong> – among students, classrooms, grade levels, schools, feeder patterns, families and communities.</p>

			<p>If you would like additional information about AVID Elementary, please contact us at <a href="mailto:avidelementary@avidcenter.org" class="inner-cont-link">avidelementary@avidcenter.org</a>.</p>

