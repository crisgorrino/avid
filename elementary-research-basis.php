	<h2 class="inner-hdr elementary">AVID Elementary Research Basis</h2>
	<div class="elementary-inner">
		
		<p>AVID’s curriculum, resources, methodologies, and strategies have always been research-based. AVID Elementary (AE) is steeped in research and practice from internationally recognized thought leaders, in subjects like motivation, student
			achievement and influence. Below is a brief overview of AE’s research basis.</p>

		<p>AE is immersed in the research of Dr. Carol Dweck, one of the world’s leading researchers in the field of motivation. In her book Mindset: <span class="italic">The New Psychology of Success,  she says:</span></p>
		
		<p class="book-quote">"In the growth mindset, people believe that their talents and abilities can be developed through passion, education, and persistence. For them, it’s not about looking smart of grooming their image. It’s about a commitment to learning—taking informed risks and learning from the results, surrounding yourself with people who will challenge you to grow, looking frankly at your deficiencies and seeking to remedy them."</p>
		
		
		<p>AE’s implementation resources, trainings, and philosophy are all grounded in the idea that the growth mindset can be
		taught to students and that it is through the growth mindset that students succeed in following their dreams and fulfilling
		their aspirations.</p>
		
		<p>AE incorporates the “best teaching practices” and research findings of Marzano, Gaddy and Dean, which have become
		critical to the entire AVID College Readiness System. This research identified nine instructional strategies with the highest
		probability of enhancing student achievement for all students, in all subjects, at all grade levels:</p>
		
		<div class="tableture cf">
			<ul class="left"> 
				<li>Identifying similarities and differences</li>
				<li>Reinforcing effort and providing recognition</li>
				<li>Nonlinguistic representations</li>
				<li>Setting goals and providing feedback</li>
				<li>Activating prior knowledge</li>
			</ul>
			
			<ul class="right">
				<li>Summarizing and note-taking</li>
				<li>Homework and practice</li>
				<li>Cooperative learning</li>
				<li>Generating and testing</li>
			</ul>
		</div>
			
		<p>AE’s certification process is based in the work of Albert Bandura’s Social Cognitive Theory, which focuses on the power of individuals to influence and educate one another through imitation and reciprocal determinism. Through this lens, the
			academic environment, the educator’s perspective, and the student’s perspective merge together within grade levels, and ultimately across the entire school.</p>

		
		<p>If you would like additional information about AVID Elementary, please contact us at <a href="mailto:avidelementary@avidcenter.org" class="inner-cont-link">avidelementary@avidcenter.org</a>.</p>
