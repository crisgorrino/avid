		<h1>Essentials</h1> 
		<p>As a foundational component of the AVID College Readiness System, AVID Elementary focuses on specific AVID Essentials to ensure quality and consistency as a transformational system.</p>
	
		<p><strong>Essential One: Instruction</strong><br>
		WICOR strategies set the framework for AVID instruction, with L-WICOR (Learning to WICOR) laying the foundation of instruction in AVID Elementary classrooms.</p>
	
		<p><strong>Essential Two: Culture</strong><br>
		AVID Elementary sites incorporate a culture of rigorous, relevant, differentiated opportunities for all students in an environment that promotes college readiness.</p>
		
		<p><strong>Essential Three: Leadership</strong><br>
		AVID Elementary leadership supports, guides, and facilitates AVID Elementary implementation for all students.</p>
		
		<p><strong>Essential Four: Systems</strong><br>
		AVID Elementary sites align their systems through the use of the Four Pillars of Excellence (accountability, articulation, assessment, and calibration) to ensure the fidelity of AVID Elementary implementation across all grades.</p>
		
		<div class="divider-cont-bottom">
			<h3>AVID Elementary Certification Process‐ Quality Assurance </h3>

		<p>To maintain AVID Elementary’s quality and ensure the Essentials are being implemented at the highest standards, AVID Elementary schools undergo an annual Certification. The certification process is intended to enhance the work of AVID Elementary educators by aligning their work to the Essentials and state standards and accountability measures, and by collecting data to inform their instruction. To become certified, an AVID Elementary school must have adopted a philosophy aligned with AVID’s mission and incorporates AVID strategies into daily expectations.</p>
		
