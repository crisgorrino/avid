		<h3>AVID Elementary Roles </h3>

		<p><strong>AVID Elementary Liaison</strong><br>
		The AE Liaison is required to attend AVID Summer Institute with AE participants and complete a two-year training cycle in AVID
		Elementary Leadership Training held by AVID Center. This role also approves data and site certification submissions to AVID Center.</p>

		<p><strong>AVID Elementary Principal</strong><br>
		The AE Principal is required to attend AVID Summer Institute with their Site Team(s) and is the instructional leader for AE Implementation and the AE Site Team. The AE Principal may attend AE Leadership Training with the AE Liaison. This role is responsible for leading the AE Site Team throughout the certification process and submits data and the certification document to the AE Liaison.</p>
		
		<p><strong>AVID Elementary Teacher</strong><br>
		The AE Teacher is required to attend the appropriate implementation stage strand at AVID Summer Institute and is the facilitator of
		learning in the classroom. This role is responsible for conducting classroom and individual assessments in a pre/post model to utilize data to inform classroom instruction. This role is an active member on the AE Site Team.</p>
		
		<h3>AVID Elementary Professional Learning </h3>
		<p><strong>AVID Elementary Liaison Leadership Training Sessions</strong><br>
			The Elementary Liaison attends two Leadership training sessions during Year 1 and two sessions during Year 2. The designated Elementary Liaison also attends the AVID Summer Institute in sequential elementary strands. Additional AE Liaisons and AE Site Principals can also attend AE Leadership Training at a discounted rate to build capacity within implementing sites.<br>
			<a href="" class="inner-cont-link">AVID Elementary Liaison Leadership Trainings Informational Document</a></p>
		
		<p><strong>AVID Summer Institute (Elementary Strands)</strong><br>
		Implementing sites attend AVID Elementary Implementation strands according to the implementation stage of AE (Beginnings,
		Foundations, or Bridges). Attendance is required the first two years of implementation. A <strong>Minimum</strong> team of four members per
		implementing site is required.</p>
		
		
		
		<p><strong>Who attends:</strong><br>
			<ul class="memb-benefits-list">
				<li>Site Principal is required</li>
